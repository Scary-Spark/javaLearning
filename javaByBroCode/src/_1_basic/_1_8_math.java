package _1_basic;

public class _1_8_math {
    public static void main(String[] args) {
        System.out.println("Pi is: " + Math.PI);
        System.out.println("E is: " + Math.E);

        System.out.println("2 to the power 3: " + Math.pow(2, 3));

        System.out.println("Abs of -2.4: " + Math.abs(-2.4));

        System.out.println("Root of 9: " + Math.sqrt(9));

        // nearest hole integer
        System.out.println("Round of 2.42 is: " + Math.round(2.42));

        // ceiling
        System.out.println("Ceiling of -2.42 is: " + Math.ceil(-2.42));

        // floor value
        System.out.println("Floor of 4.9 is: " + Math.floor(4.9));

        System.out.println("Max of 2 and 3: " + Math.max(2, 3));
        System.out.println("Min of 2 and 3: " + Math.min(2, 3));
    }
}
